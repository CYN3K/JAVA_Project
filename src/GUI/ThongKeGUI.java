    public ArrayList<HoaDonDTO> taohoadontheoquy(int c) {
    	int start=0,end=0;
    	ArrayList<HoaDonDTO> hdc = new ArrayList();
    	switch(c){
    		case 1:{
    			start=1;
    			end=3;
    			break;
    		}
    		case 2:{
    			start=4;
    			end=6;
    			break;
    		}
    		case 3:{
    			start=7;
    			end=9;
    			break;
    		}
    		case 4:{
    			start=10;
    			end=12;
    			break;
    		}
    		default:break;
    	}
    	for(HoaDonDTO s: hdlist) {
    		String date = s.getNgayLap();
    		String[] split = new String[3];
    		split=date.split("-");
    		if(Integer.parseInt (split[1]) >= start && Integer.parseInt (split[1]) <= end) {
    			hdc.add(s);
    		}
    	}
    	return hdc;
    }
    public ArrayList<HoaDonDTO> taohoadontheonam(int c) {
    	int year=0;
    	ArrayList<HoaDonDTO> hdc = new ArrayList();
    	switch(c){
    		case 1:{
    			year=2019;
    			break;
    		}
    		case 2:{
    			year=2020;
    			break;
    		}
    		case 3:{
    			year=2021;
    			break;
    		}
    		default:break;
    	}
    	for(HoaDonDTO s: hdlist) {
    		String date = s.getNgayLap();
    		String[] split = new String[3];
    		split=date.split("-");
    		if(Integer.parseInt (split[0]) ==year) {
    			hdc.add(s);
    		}
    	}
    	return hdc;
    }
