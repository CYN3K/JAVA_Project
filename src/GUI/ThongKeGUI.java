    public void loadTKSP(ArrayList<TKSanPham> tksp){
        Vector data;
        model.setRowCount(0);
        for(TKSanPham tk1 : tksp){
            data=new Vector();
            data.add(tk1.getMaSP());
            data.add(tk1.getTenSP());
            data.add(tk1.getSlNhap());
            data.add(tk1.getSlBan());
            data.add(tk1.getTongNhap());
            data.add(tk1.getTongBan());
            data.add(tk1.getLoinhuan());
            model.addRow(data);
        }
        tbTKSP.setModel(model);
    }

